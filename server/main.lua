Citizen.CreateThread(function()
    if string.upper(Config.Framework) == "QBCORE" then

        if Config.Debug then
            print("^2FRAMEWORK SET TO ^4QBCORE")
        end
    
        QBCore = exports['qb-core']:GetCoreObject()

    elseif string.upper(Config.Framework) == "ESX" then

        if Config.Debug then
            print("^2FRAMEWORK SET TO ^4ESX")
        end

        ESX = exports["es_extended"]:getSharedObject()
    
    else
    
        if Config.Debug then
            print("^1ERROR! FRAMEWORK IS ^3NOT ^1SUPPORTED")
            print("^3" .. string.upper(GetCurrentResourceName()) .. " ^1HAS BEEN STOPPED!!!")
        end
    
        return
    
    end
end)

-- == -- Scripts -- == --

RegisterNetEvent("dd_namechanger:server:changeName", function(data)

    local src = source

    if string.upper(Config.Framework) == "QBCORE" then

        local Player = QBCore.Functions.GetPlayer(src)

        if Player.PlayerData.money["cash"] >= Config.Cost then

            Player.Functions.RemoveMoney("cash", Config.Cost)

            local charinfo = Player.PlayerData.charinfo
            
            charinfo.firstname = data.firstname
            charinfo.lastname = data.lastname

            MySQL.update.await('UPDATE players SET charinfo = ? WHERE citizenid = ?', { json.encode(charinfo), Player.PlayerData.citizenid })

        else

            TriggerClientEvent("dd_namechanger:client:notify", src, {msg = translate("notification_no_money"), duration = 5000, msgType = "error"})

        end

    elseif string.upper(Config.Framework) == "ESX" then

        local xPlayer = ESX.GetPlayerFromId(src)

        if xPlayer.getMoney() >= Config.Cost then
            
            xPlayer.removeMoney(Config.Cost)

            MySQL.update.await('UPDATE users SET firstname = ?, lastname = ? WHERE identifier = ?', { data.firstname, data.lastname, xPlayer.identifier })

            TriggerClientEvent("dd_namechanger:client:notify", src, {msg = translate("notification_success"), duration = 5000, msgType = "success"})

        else

            TriggerClientEvent("dd_namechanger:client:notify", src, {msg = translate("notification_no_money"), duration = 5000, msgType = "error"})

        end

    end

end)