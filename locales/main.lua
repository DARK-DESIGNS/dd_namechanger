Language = {}

function translate(text, ...)
    if text then 
        local string = Language[Config.Language][text]
        if string then 
            return string.format(string, ...)
        else    
            if Config.Debug then
                print("^1ERROR WITH LOCAL: " .. text)
            end
            return "Error with translation: " .. text
        end
    else
        if Config.Debug then
            print("^1ERROR WITH LOCAL")
        end
        return "Error with translation"
    end
end